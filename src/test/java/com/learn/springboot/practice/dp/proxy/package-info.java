package com.learn.springboot.practice.dp.proxy;
/**
 *
 *** 代理模式包含角色：
 ************* Subject: 抽象主题角色 ***********************************************************************
 ************* Proxy: 代理主题角色   ***********************************************************************
 ************* RealSubject: 真实主题角色 *******************************************************************
 * *******************************************************************************************************
 *   优点：代理可以协调调用方与被调用方，降低了系统的耦合度。根据代理类型和场景的不同，可以起到控制安全性、减小系统开销等作用。
 *   缺点：增加了一层代理处理，增加了系统的复杂度，同时可能会降低系统的相应速度。
 ********************************************************************************************************/